&nbsp&nbsp&nbsp&nbsp&nbsp1. the establishment and operations of new and existing transportation terminals that charge fees and require compulsory use by public utility &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspvehicles;<br>
&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp2. the enforcement of re-routing schemes that violate the authorized route as provided for the PUV franchises;<br>
