Relevant to the impact on additional fares from the pier/town proper to the Bus Terminal, a sworn statement of two (2) regular travellers, who are personally aggrieved of the prevailing situation, have submitted to the team their notarized comments in particular are attached for record and reference purposes.<br><br>

In reply to our letter dated August 6, 2008, requesting of the action taken, if there is any in so far as the above cited provision of EO No. 712, states that, relative to this:  Please be informed that the Department is not yet implementing Section 2 of the subject EO, due to non-issuance of its implementing guidelines.  Moreover, DILG and DOTC is in the process of formulating the implementing guidelines through a Joint Memorandum Circular to be issued soonest.  Immediate implementation at their level is subject to receipt of said guidelines.<br><br>

Notwithstanding the certification issued by the Municipal Engineer that the construction of Bulan Common Bus Terminal and abattoir under contract with S.R. Baldon Construction & Supply and Steven Construction & Supply, respectively, has been implemented, but construction of the said projects are still on-going as of July 24, 2007, which revealed probable liquidated damages to be determined by the TAS RTSO of COA Region V, Legaspi City.<br><br>

